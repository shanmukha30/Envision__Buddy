package com.teaminversion.envisionbuddy;

public class ImagesItem{
	private String uid;
	private int size;
	private int width;
	private String url;
	private int height;

	public String getUid(){
		return uid;
	}

	public int getSize(){
		return size;
	}

	public int getWidth(){
		return width;
	}

	public String getUrl(){
		return url;
	}

	public int getHeight(){
		return height;
	}
}
