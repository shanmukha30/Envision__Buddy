package com.teaminversion.envisionbuddy;

public class Extra{
	private Object textureCount;
	private int size;
	private String type;
	private Object textureMaxResolution;
	private Object faceCount;
	private Object vertexCount;

	public Object getTextureCount(){
		return textureCount;
	}

	public int getSize(){
		return size;
	}

	public String getType(){
		return type;
	}

	public Object getTextureMaxResolution(){
		return textureMaxResolution;
	}

	public Object getFaceCount(){
		return faceCount;
	}

	public Object getVertexCount(){
		return vertexCount;
	}
}
