package com.teaminversion.envisionbuddy;

public class Glb{
	private int textureCount;
	private int size;
	private String type;
	private int textureMaxResolution;
	private int faceCount;
	private int vertexCount;

	public int getTextureCount(){
		return textureCount;
	}

	public int getSize(){
		return size;
	}

	public String getType(){
		return type;
	}

	public int getTextureMaxResolution(){
		return textureMaxResolution;
	}

	public int getFaceCount(){
		return faceCount;
	}

	public int getVertexCount(){
		return vertexCount;
	}
}
